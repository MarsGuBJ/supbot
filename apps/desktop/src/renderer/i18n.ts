export function translate(language: Language, key: string, vars?: Record<string, string | number>): string {
  const template = language === "zh" ? zh[key] || key : key;
  if (!vars) {
    return template;
  }
  return Object.entries(vars).reduce((text, [name, value]) => text.replaceAll(`{${name}}`, String(value)), template);
}

export function loadLanguage(): Language {
  try {
    const stored = window.localStorage.getItem("hbclient.language") || window.localStorage.getItem("supbot.language");
    return stored === "en" ? "en" : "zh";
  } catch {
    return "zh";
  }
}

export function saveLanguage(language: Language): void {
  try {
    window.localStorage.setItem("hbclient.language", language);
  } catch {
    // Ignore storage failures; the in-memory language still changes.
  }
}
